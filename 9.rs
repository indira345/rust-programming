/*
9. Write a program to create different types of constants print it in the output
*/

const S: &str = "rust";
const C: char = 'r';
const A: i32 = 15;
const F: f64 = 23.59;
const B: bool = true;
fn main() {
    println!("S = {}", S);
    println!("C = {}", C);
    println!("A = {}", A);
    println!("F = {}", F);
    println!("B = {}", B);
}
